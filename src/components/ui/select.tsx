import * as React from "react";
import * as SelectPrimitive from "@radix-ui/react-select";
import { Check, ChevronDown, ChevronUp } from "lucide-react";
import { cn } from '@/lib/utils';
import { useAccessibility } from '@/contexts/accessibility-context';
import { useFocusable } from '@/contexts/keyboard-navigation';

interface SelectRootProps extends React.ComponentPropsWithoutRef<typeof SelectPrimitive.Root> {
  error?: string;
  description?: string;
}

const Select = React.forwardRef<
  React.ElementRef<typeof SelectPrimitive.Root>,
  SelectRootProps
>(({ error, description, children, onValueChange, ...props }, ref) => {
  const { announce } = useAccessibility();

  // Announce value changes to screen readers
  const handleValueChange = (value: string) => {
    announce(`Selected: ${value}`, 'polite');
    onValueChange?.(value);
  };

  return (
    <SelectPrimitive.Root {...props} onValueChange={handleValueChange}>
      {children}
    </SelectPrimitive.Root>
  );
});
Select.displayName = "Select";

const SelectGroup = SelectPrimitive.Group;

const SelectValue = SelectPrimitive.Value;

interface SelectTriggerProps extends React.ComponentPropsWithoutRef<typeof SelectPrimitive.Trigger> {
  error?: string;
  'aria-describedby'?: string;
  'aria-errormessage'?: string;
}

const SelectTrigger = React.forwardRef<
  React.ElementRef<typeof SelectPrimitive.Trigger>,
  React.ComponentPropsWithoutRef<typeof SelectPrimitive.Trigger>
>(({ className, children, ...props }, ref) => {
  const triggerRef = useFocusable(0, 'select');
  const { announce } = useAccessibility();

  // Combine refs
  const combinedRef = React.useCallback(
    (node: React.ElementRef<typeof SelectPrimitive.Trigger> | null) => {
      if (typeof ref === 'function') ref(node);
      else if (ref) ref.current = node;
      if (triggerRef) triggerRef.current = node;
    },
    [ref, triggerRef]
  );

  // Announce state changes
  const handleStateChange = (open: boolean) => {
    if (open) {
      announce(`${children} select expanded`, 'polite');
    } else {
      announce(`${children} select collapsed`, 'polite');
    }
  };

  return (
    <SelectPrimitive.Trigger
      ref={combinedRef}
      className={cn(
        "flex h-10 w-full items-center justify-between rounded-md border border-input bg-background px-3 py-2 text-sm ring-offset-background placeholder:text-muted-foreground disabled:cursor-not-allowed disabled:opacity-50",
        "focus-visible-ring",
        "high-contrast-text",
        "reduced-motion-safe",
        className
      )}
      onSelect={(event) => {
        handleStateChange(event.type === 'select');
        props.onSelect?.(event);
      }}
      {...props}
    >
      {children}
      <SelectPrimitive.Icon asChild>
        <ChevronDown className="h-4 w-4 opacity-50" />
      </SelectPrimitive.Icon>
    </SelectPrimitive.Trigger>
  )
});
SelectTrigger.displayName = SelectPrimitive.Trigger.displayName;

